
public class Calculator {
	
	
	public double add(double n1,double n2){
		
		
		double result=n1+n2;
		return result;
	}
	
	public double subtract(double n1, double n2){
		
		double result=n1-n2;
		return result;
		
	}
	
	
	public double multiply(double a,double b){
		double result=a*b;
		return result;
	}
	
	
	public double divide(double a, double b){
		double result=a/b;
		return result;
		
	}

}
